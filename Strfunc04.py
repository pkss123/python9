# 문자열 변경
# .lower(), .upper() - 대문자를 소문자로, 소문자를 대문자로 변경
# .swapcase() - 대소문자를 서로 변경
# capitalize() - 문자열의 첫 글자만 대문자로 변경
# title() - 문자열 내부의 모든 단어의 첫글자를 대문자로 변경

s = "Good afternoon! my name is CHAE"
print(s.lower())
print(s.upper())
print(s.swapcase())
print(s.capitalize())
print(s.title())
s = s.upper()
print(s)